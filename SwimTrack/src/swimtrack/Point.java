package swimtrack;

/**
 *
 * @author Matt
 */
public class Point {
    private Long x;
    private double y;
    
    public Point(Long x, double y){
        this.x = x;
        this.y = y;
        
        
    }

    public Long getX() {
        return x;
    }

    public void setX(Long x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
    
    
    
    
    
    
    
    
}
